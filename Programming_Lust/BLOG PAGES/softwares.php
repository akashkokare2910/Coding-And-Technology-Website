                <p class="card-text"> it is critical to implement software without major flaws, or 'bugs.' Eindhoven University of Technology Ph.D. candidate Thomas Neele developed three techniques for smarter and faster bug checking, based on the model checking method. Model checking is one of the most rigorous techniques to check software. It looks at all possible things a software system can do, and the 'states' it can be in, to check if it works as required. The challenge is that software often consists of many parts that work in parallel. That can cause an explosion in the number of states that need to be investigated, making model checking costly and perhaps even unmanageable. Neele explored new ways to address this problem by reducing the number of states that need to be checked. This all happens in three reduction techniques. The first technique reduces the number of states by grouping similar states together. The second approach checks whether the parts that work in parallel sometimes perform a task independently. In that case, it is not necessary to investigate all possible states. The last technique checks whether certain data elements are relevant and removes them if they're not.

